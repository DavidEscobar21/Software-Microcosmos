    private void ActualizarParametro(){
       
            DefaultTableModel t = new DefaultTableModel();
            Tbl_Parametros.setModel(t);
            t.addColumn("Id");
            t.addColumn("Cai");
            t.addColumn("Fecha Emisi칩n");
            t.addColumn("Fecha Caducidad");
            t.addColumn("Factura Inicial");
            t.addColumn("Factura Final");
            t.addColumn("Estado");
        
            List<Parametros> parametros = this.daoParametros.findParametrosEntities();
        
            String s;
            for(Parametros Parametros : parametros){
                
                if(Parametros.isActivoParametros() == true){
                s = "Activado";
                }else{
                s = "Desactivado";
                }
                t.addRow(
                    new Object[]{
                        Parametros.getIdParametros(),
                        Parametros.getCai(),
                        MostrarFecha(Parametros.getFechaEmision().toString()),
                        MostrarFecha(Parametros.getFechaCaducidad().toString()),
                        Parametros.getFacturaInicial(),
                        Parametros.getFacturaFinal(),
                        s
                    });
            }
       
       
       }

      private void LlenarParametro(){
        
       if(Txt_Cai.getText().length() < 32){
        
        JOptionPane.showMessageDialog(this, "El Cai debe de contener 32 caracteres","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(ValidacionCAI(Txt_Cai.getText()) == false){
        
        JOptionPane.showMessageDialog(this, "Formato de Cai inv치lido, el formato es: XXXXXX-XXXXXX-XXXXXX-XXXXXX-XXXXXX-XX","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(ValidacionDeRepetidos(Txt_Cai.getText()) == true){
        
        JOptionPane.showMessageDialog(this, "Este Cai ya existe","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Txt_FacturaInicial.getText().length() < 1){
        
        JOptionPane.showMessageDialog(this, "El campo de Factura Inicial no puede ser nulo","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Txt_FacturaFinal.getText().length() < 1){
        
        JOptionPane.showMessageDialog(this, "El campo de Factura Final no puede ser nulo","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Integer.parseInt(Txt_FacturaInicial.getText()) >= Integer.parseInt(Txt_FacturaFinal.getText())){
        
        JOptionPane.showMessageDialog(this, "La Factura Inicial no puede ser mayor o igual a la Factura Final","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(ValidacionFecha(Txt_FechaEmision.getText()) == false || Txt_FechaEmision.getText().length() < 10){
        
        JOptionPane.showMessageDialog(this, "Formato de Fecha Emisi칩n inv치lido, el formato es: DD/MM/YYYY","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Fechavalida(Txt_FechaEmision.getText()) == false){
        
        JOptionPane.showMessageDialog(this, "Formato de Fecha Emisi칩n inv치lido, el formato es: DD/MM/YYYY","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(ValidacionFecha(Txt_FechaCaducidad.getText())== false || Txt_FechaCaducidad.getText().length() < 10){
        
        JOptionPane.showMessageDialog(this, "Formato de Fecha Caducidad inv치lido, el formato es: DD/MM/YYYY","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Fechavalida(Txt_FechaCaducidad.getText()) == false){
        
        JOptionPane.showMessageDialog(this, "Formato de Fecha Caducidad inv치lido, el formato es: DD/MM/YYYY","Error",JOptionPane.ERROR_MESSAGE);
        
       }else if(Comparacionfecha(Txt_FechaEmision.getText(), Txt_FechaCaducidad.getText()) == false){
        
        JOptionPane.showMessageDialog(this, "La Fecha Emisi칩n no puede ser mayor o igual a la Fecha Caducidad","Error",JOptionPane.ERROR_MESSAGE);
        
       }else{
       objParametros.setIdParametros(IdParametro());
       objParametros.setCai(Txt_Cai.getText());
       objParametros.setFacturaInicial(Integer.parseInt(Txt_FacturaInicial.getText()));
       objParametros.setFacturaFinal(Integer.parseInt(Txt_FacturaFinal.getText()));
       objParametros.setFechaEmision(Timestamp.valueOf(GuardarFecha(Txt_FechaEmision.getText())+ " 00:00:00"));
       objParametros.setFechaCaducidad(Timestamp.valueOf(GuardarFecha(Txt_FechaCaducidad.getText())+ " 00:00:00"));
       objParametros.setActivoParametros(true);
     
        
        try {
            daoParametros.create(objParametros);
            ActualizarParametro();
            LimpiarParametros();
            JOptionPane.showMessageDialog(this, "Se guard칩 correctamente");
        } catch(Exception ex){
        
        log.Logs("PantallaParametros", "Error al a침adir: " + ex);
          
        }
       }   
       }
    
