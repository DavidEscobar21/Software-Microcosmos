    public static String MostrarFecha(String f){
         
        String dia = f.substring(0, 4);
        String mes = f.substring(5, 7);
        String año = f.substring(8, 10);
        String hora = f.substring(11, 19);
        
        return año+"/"+mes+"/"+dia +" "+hora;
    
    }
    
    
