    private void Txt_TelefonoEmpleadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Txt_TelefonoEmpleadoKeyTyped
         
        char c = evt.getKeyChar();
        String Texto = Txt_TelefonoEmpleado.getText();
       
        if(c < '0' || c > '9') {
        
            //consume no valida los datos
            evt.consume();
                     
        }
        
        if (Txt_TelefonoEmpleado.getText().length() == 0 && (c == '0' || c == '4' || c == '5' || c == '6' || c == '1') ){
       
        evt.consume();
        JOptionPane.showMessageDialog(this, "El número de teléfono debe de comenzar con 2, 3, 7, 8 ó 9","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        
        
        if (Txt_TelefonoEmpleado.getText().length() >= 8){
        
        evt.consume();
        
        }
        
        if((evt.getKeyChar() == 22)){
        
            Txt_TelefonoEmpleado.setText(Texto.substring(0, 8));
                    
        }
        
    }//GEN-LAST:event_Txt_TelefonoEmpleadoKeyTyped

