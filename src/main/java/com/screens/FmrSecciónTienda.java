    private void Txt_DescripcionSeccionTiendaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Txt_DescripcionSeccionTiendaKeyTyped
        
        char c = evt.getKeyChar();
        String Texto = Txt_DescripcionSeccionTienda.getText();
        
        
        if((evt.getKeyChar() == 22)){
        
            Txt_DescripcionSeccionTienda.setText(Texto.substring(0, 45));
                    
        }
        
        if (Txt_DescripcionSeccionTienda.getText().length() >= 45){
        
        evt.consume();
        
        }
        
        if (Txt_DescripcionSeccionTienda.getText().length() == 1){

            char mayuscula = Texto.charAt(0);
            Texto = Character.toUpperCase(mayuscula)+ Texto.substring(1,Texto.length());
            Txt_DescripcionSeccionTienda.setText(Texto);

        }
        
    }//GEN-LAST:event_Txt_DescripcionSeccionTiendaKeyTyped

