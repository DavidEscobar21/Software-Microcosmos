    private void ActualizarSeccionTienda(){
       
            DefaultTableModel t = new DefaultTableModel();
            Tbl_SeccionTienda.setModel(t);
            t.addColumn("Id");
            t.addColumn("Nombre");
            t.addColumn("Descripción");
            t.addColumn("Estado");
        
            List<SeccionTienda> seccionTienda = this.daoSeccionTienda.findSeccionTiendaEntities();
        
            String s;
            for(SeccionTienda SeccionTienda : seccionTienda){
                
                if(SeccionTienda.isActivoSeccionTienda() == true){
                s = "Activado";
                }else{
                s = "Desactivado";
                }
                t.addRow(
                    new Object[]{
                        
                        SeccionTienda.getIdSeccionTienda(),
                        SeccionTienda.getNombreSeccionTienda(),
                        SeccionTienda.getDescripcionSeccionTienda(),
                        s
                    });
            }
       
       
       }
    
       private void LimpiarSeccionTienda(){
           
       try{
       if(ValidarAgregar(N.id) == true){
        Btn_Añadir.setEnabled(true);
        }
       Btn_Editar.setEnabled(false);
       Btn_Activar_Desactivar.setEnabled(false);
       Txt_IdSeccionTienda.setText("");
       Txt_NombreSeccionTienda.setText("");
       Txt_DescripcionSeccionTienda.setText("");
       }catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al limpiar: " + ex);
          
        }
       
       }
    
       private void Activar_Desactivar(){
        
        int fila = Tbl_SeccionTienda.getSelectedRow();
        
        String a = Txt_Activo.getText().toString();
       
        if(a.equals("Activado")){
                  
        objSeccionTienda.setIdSeccionTienda(Integer.parseInt(Txt_IdSeccionTienda.getText()));
        objSeccionTienda.setNombreSeccionTienda(Tbl_SeccionTienda.getValueAt(fila, 1).toString());
        objSeccionTienda.setDescripcionSeccionTienda(Tbl_SeccionTienda.getValueAt(fila, 2).toString());
        objSeccionTienda.setActivoSeccionTienda(false);    
        
        
        try {
            daoSeccionTienda.edit(objSeccionTienda);
            ActualizarSeccionTienda();
            Btn_Activar_Desactivar.setText("Activar");
            JOptionPane.showMessageDialog(this, "Se desactivó correctamente");
        } catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al desactivar: " + ex);
          
        }
        
        
        LimpiarSeccionTienda();
        
        }else{
        
        objSeccionTienda.setIdSeccionTienda(Integer.parseInt(Txt_IdSeccionTienda.getText()));
        objSeccionTienda.setNombreSeccionTienda(Tbl_SeccionTienda.getValueAt(fila, 1).toString());
        objSeccionTienda.setDescripcionSeccionTienda(Tbl_SeccionTienda.getValueAt(fila, 2).toString());
        objSeccionTienda.setActivoSeccionTienda(true); 
        
        try {
            daoSeccionTienda.edit(objSeccionTienda);
            ActualizarSeccionTienda();
            Btn_Activar_Desactivar.setText("Desactivar");
            JOptionPane.showMessageDialog(this, "Se activó correctamente");
        } catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al activar: " + ex);
          
        }
        
        LimpiarSeccionTienda();
        
        }
        
        }
       
       
       private void LlenarSeccionTienda(){
        
        if(Txt_NombreSeccionTienda.getText().length() < 3){
        
        
        JOptionPane.showMessageDialog(this, "El nombre tiene que contener al menos 3 letras","Error",JOptionPane.ERROR_MESSAGE);
        
        }else if(ValidacionDeRepetidos(Txt_NombreSeccionTienda.getText()) == true){
        
        JOptionPane.showMessageDialog(this, "Este elemento ya existe","Error",JOptionPane.ERROR_MESSAGE);
        
        }else if(ValidacionTresLetras(Txt_NombreSeccionTienda.getText()) == true){
        
        JOptionPane.showMessageDialog(this, "No se pueden repetir 3 letras seguidas","Error",JOptionPane.ERROR_MESSAGE);
        
        }else if(Txt_DescripcionSeccionTienda.getText().length() < 8){
        
        JOptionPane.showMessageDialog(this, "La descripción tiene que contener al menos 8 letras","Error",JOptionPane.ERROR_MESSAGE);
        
        }
        else{
       
       objSeccionTienda.setIdSeccionTienda(IdSeccionTienda());
       objSeccionTienda.setNombreSeccionTienda(Txt_NombreSeccionTienda.getText());
       objSeccionTienda.setDescripcionSeccionTienda(Txt_DescripcionSeccionTienda.getText());
       objSeccionTienda.setActivoSeccionTienda(true); 
        
        try {
            daoSeccionTienda.create(objSeccionTienda);
            ActualizarSeccionTienda();
            LimpiarSeccionTienda();
            JOptionPane.showMessageDialog(this, "Se guardó correctamente");
        } catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al añadir: " + ex);
          
        }
       }   
       }
       
       
       private void EditarSeccionTienda(){
                  
           
        if(Txt_NombreSeccionTienda.getText().length() < 3){
        
        JOptionPane.showMessageDialog(this, "El nombre tiene que contener al menos 3 letras","Error",JOptionPane.ERROR_MESSAGE);
             
        }else if(ValidacionTresLetras(Txt_NombreSeccionTienda.getText()) == true){
        
        JOptionPane.showMessageDialog(this, "No se pueden repetir 3 letras seguidas","Error",JOptionPane.ERROR_MESSAGE);
        
        }else if(Txt_DescripcionSeccionTienda.getText().length() < 8){
        
        JOptionPane.showMessageDialog(this, "La descripción tiene que contener al menos 8 letras","Error",JOptionPane.ERROR_MESSAGE);
        
        }else{
            
       objSeccionTienda.setIdSeccionTienda(Integer.parseInt(Txt_IdSeccionTienda.getText()));
       objSeccionTienda.setNombreSeccionTienda(Txt_NombreSeccionTienda.getText());
       objSeccionTienda.setDescripcionSeccionTienda(Txt_DescripcionSeccionTienda.getText());
      
       String a = Txt_Activo.getText().toString();
       if(a.equals("Activado")){
          objSeccionTienda.setActivoSeccionTienda(true);
       }else{
          objSeccionTienda.setActivoSeccionTienda(false);
            }
       
        try {
            daoSeccionTienda.edit(objSeccionTienda);
            ActualizarSeccionTienda();
            JOptionPane.showMessageDialog(this, "Se actualizó correctamente");
        } catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al editar: " + ex);
          
        }
        }
       }
       
       
        public static int IdSeccionTienda(){
        
             EntityManagerFactory emf = Persistence.createEntityManagerFactory("DB");
             EntityManager em = emf.createEntityManager();
      
             String select = "SELECT Max(idSeccionTienda)+1 FROM SeccionTienda";
   
             Query query = em.createQuery(select);
        
              if(query.getSingleResult() == null){
             
             return 1;
             
             }else{
                 
             return Integer.parseInt(query.getSingleResult().toString());
             
             }
        
        }
       
        public static boolean ValidacionDeRepetidos(String Nombre){
       
         EntityManagerFactory emf = Persistence.createEntityManagerFactory("DB");
         EntityManager em = emf.createEntityManager();
      
             String select = "SELECT idSeccionTienda FROM SeccionTienda WHERE nombreSeccionTienda  = '"+Nombre+ "'";
   
             Query query = em.createQuery(select);
       
             if(query.getResultList().size() == 0){
             
             return false;
             
             }else{
             
             return true;
                
             }
             
        }
        
        private static boolean ValidacionTresLetras(String Nombre){
        
            
        if(Nombre.length() >= 3){
        String Letra1 = Nombre.substring(0, 1);
        String Letra2 = Nombre.substring(1, 2);
        String Letra3 = Nombre.substring(2, 3);
        
        
        if(Letra1.equalsIgnoreCase(Letra2) && Letra2.equalsIgnoreCase(Letra3)){
        
        return true;
         
        }else{
        
        return false;
              
        }
        }else{
        
            return false;
        
        }
              
        }
       
    private void GenerarReporte(){
       
        
             Connection con;
             
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            
        try{
        
        con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Microcosmos?zeroDateTimeBehavior=convertToNull", "root", "12345");
        
        JasperReport report = (JasperReport) JRLoader.loadObject(getClass().getResource("/Reportes/ReporteSeccionTienda.jasper"));
        
        Map parametros = new HashMap<String, Object>();
        
        parametros.put("Empleado", N.getNombre());
   
        JasperPrint jprint = JasperFillManager.fillReport(report,parametros, con );
        JasperViewer view = new JasperViewer(jprint,false);
        view.setTitle("Reporte Articulos");
        Image icon = new ImageIcon(getClass().getResource("/imagenes/IconoMicrocosmos.png")).getImage();
        view.setIconImage(icon);
        

        final JRViewer viewer = new JRViewer(jprint);
            JRSaveContributor[] contrbs = viewer.getSaveContributors();

            for (JRSaveContributor saveContributor : contrbs)
            {
                if (!(saveContributor instanceof net.sf.jasperreports.view.save.JRSingleSheetXlsSaveContributor
                    || saveContributor instanceof net.sf.jasperreports.view.save.JRPdfSaveContributor))
            viewer.removeSaveContributor(saveContributor);
        }
        view.setContentPane(viewer);
        view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        view.setVisible(true);
        

        view.show();

      
    }catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al generar reporte: " + ex);
          
        }
        }catch(Exception ex){
        
        log.Logs("PantallaSeccionTienda", "Error al generar reporte: " + ex);
          
        }
                
          }
    
    
